    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        //mengambil format date
        //SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        //String tgl = String.valueOf(format.format(tgl_pemasangan.getDate()));

        
        //string query insert
        String sql = "insert into db_barang values (?,?,?,?)";
        try{
            PreparedStatement stat = Conn.prepareStatement(sql);
            stat.setString(1, id_barang.getText());
            stat.setString(2, nama_barang.getText());
            stat.setString(3, stok.getText());
            stat.setString(4, keterangan.getText());
            

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data akan disimpan");
            clear();
            id_barang.requestFocus();
            tableM();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
            tableM();
        }
    }//GEN-LAST:event_simpanActionPerformed


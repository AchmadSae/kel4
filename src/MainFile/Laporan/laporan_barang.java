      public void tableM(){
        Object baris[]={"ID Barang", "Nama Barang", "Jumlah Stok", "Keterangan"};
        tablemode = new DefaultTableModel(null, baris);
       tableM.setModel(tablemode);
        String sql = " select * from db_barang order by id_barang";
        try{
            java.sql.Statement put = Conn.createStatement();
            ResultSet hasil = put.executeQuery(sql);
            while (hasil.next()){
                String id_barang = hasil.getString("id_barang");
                String nama = hasil.getString("nama_barang");
                String stok = hasil.getString("stok");
                String ket = hasil.getString("keterangan");
                
                String hasilAll[] = {id_barang, nama, stok, ket};
                tablemode.addRow(hasilAll);
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Tabel Instalasi Tidak Terkoneksi" + e);
        }
        
    }
    

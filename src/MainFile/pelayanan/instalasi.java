    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        //mengambil format date
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = String.valueOf(format.format(tgl_pemasangan.getDate()));

        
        //string query insert
        String sql = "insert into db_instalasi values (?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = Conn.prepareStatement(sql);
            stat.setString(1, id_pelanggan.getText());
            stat.setString(2, tgl);
            stat.setString(3, alamat.getText());
            stat.setString(4, nama_pemilik.getText());
            stat.setString(5, spare_part.getText());
            stat.setString(6, proses.getText());  
            stat.setString(7, status.getSelectedItem().toString());
            

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data akan disimpan");
            clear();
            id_pelanggan.requestFocus();
            tableM();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
            tableM();
        }
    }//GEN-LAST:event_simpanActionPerformed


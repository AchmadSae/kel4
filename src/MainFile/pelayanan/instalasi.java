      public void tableM(){
        Object baris[]={"ID Pelanggan", "Tanggal Pemasangan", "Alamat Pemsangan", "Nama Pemilik", "Spare Part", "Proses", "Status"};
        tablemode = new DefaultTableModel(null, baris);
       tableM.setModel(tablemode);
        String sql = " select * from db_instalasi order by id_pelanggan";
        try{
            java.sql.Statement put = Conn.createStatement();
            ResultSet hasil = put.executeQuery(sql);
            while (hasil.next()){
                String id_instalasi = hasil.getString("id_pelanggan");
                String tgl = hasil.getString("tgl_pemasangan");
                String alamat = hasil.getString("alamat");
                String nama = hasil.getString("nama");
                String sparepart = hasil.getString("spare_part");
                String proses = hasil.getString("proses");
                String status = hasil.getString("status");
                
                
                String hasilAll[] = {id_instalasi, tgl, alamat, nama, sparepart, proses , status};
                tablemode.addRow(hasilAll);
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Tabel Instalasi Tidak Terkoneksi" + e);
        }
        
    }
    

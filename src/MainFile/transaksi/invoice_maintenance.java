    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        //mengambil format date
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = String.valueOf(format.format(tgl_tempo.getDate()));

        
        //string query insert
        String sql = "insert into db_invoice_maintenance values (?,?,?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = Conn.prepareStatement(sql);
            stat.setString(1, invoice.getText());
            stat.setString(2, tgl);
            stat.setString(3, alamat.getText());
            stat.setString(4, nama_pemilik.getText());
            stat.setString(5, pesan.getText());
            stat.setString(6, kunjungan.getText());
            stat.setString(7, biaya_kunjungan.getText());
            stat.setString(8, spare_part.getText());
            stat.setString(9, biaya_sparePart.getText());
            stat.setString(10, status.getSelectedItem().toString());
            stat.setString(11, biaya.getText());
            

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data akan disimpan");
            clear();
            invoice.requestFocus();
            tableM();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
            tableM();
        }
    }//GEN-LAST:event_simpanActionPerformed


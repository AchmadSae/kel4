    private void tableMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMMouseClicked
        SimpleDateFormat tglview = new SimpleDateFormat("yyyy-MM-dd");  
        Date dateView = null;
        int baris = tableM.getSelectedRow();
        try {
                dateView = tglview.parse((String) tableM.getValueAt(baris, 1));
            }
        catch (ParseException ex) {
                Logger.getLogger(invoice_maintenance.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        invoice.setText(tableM.getModel().getValueAt(baris, 0).toString()); 
        tgl_tempo.setDate(dateView);
        alamat.setText(tableM.getModel().getValueAt(baris, 2).toString());
        nama_pemilik.setText(tableM.getModel().getValueAt(baris, 3).toString());
        pesan.setText(tableM.getModel().getValueAt(baris, 4).toString());
        kunjungan.setText(tableM.getModel().getValueAt(baris, 5).toString());
        biaya_kunjungan.setText(tableM.getModel().getValueAt(baris, 6).toString());
        spare_part.setText(tableM.getModel().getValueAt(baris, 7).toString());
        biaya_sparePart.setText(tableM.getModel().getValueAt(baris, 8).toString());
        status.setSelectedItem(tableM.getModel().getValueAt(baris, 9).toString());
        biaya.setText(tableM.getModel().getValueAt(baris, 10).toString());
    }//GEN-LAST:event_tableMMouseClicked

